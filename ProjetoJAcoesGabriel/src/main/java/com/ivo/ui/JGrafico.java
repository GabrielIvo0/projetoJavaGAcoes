    private void MontaGrafico(String nomeG){
        int periodo;
        String SemDia = jComboPER.getSelectedItem()+"";
        int SemDiaV = 0;
        String stC = jCaixaTexto.getText();
        periodo = Integer.parseInt(stC.substring(0));
        int marcD;
        if(jCheckMarca.isSelected())marcD = 1;
        else marcD = 0;
        if(SemDia.equals("Semanal")){
                SemDiaV =1;
                periodo = periodo*7;
        }
        
        for (Component gAtual: jPanel3.getComponents()){       
            jPanel3.remove(gAtual);
        }
        
        System.out.println(nomeG);
        File Ldir = new File(this.diretorioG);
        File []arqdir = Ldir.listFiles();
        for (File file : arqdir){
            if(nomeG.equals(getSimbolo(file.getName()))){
            
                try {
                    jPanel3.setLayout(new java.awt.BorderLayout());
                    jPanel3.add(gf.Fgrafico(file.getAbsolutePath(),getSimbolo(file.getName()), periodo,SemDiaV,marcD));
                } catch (FileNotFoundException ex){
                    Logger.getLogger(JGrafico.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        jPanel3.validate();
    }
