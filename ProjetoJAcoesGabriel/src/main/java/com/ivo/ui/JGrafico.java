    private void incluirAtivos(){
        File dir = new File(this.diretorioG);
        File [] arquivos = dir.listFiles();
        String nmArq;
        for (File arquivo : arquivos) {
            nmArq = arquivo.getName();
            if (nmArq.substring(nmArq.lastIndexOf(".")+1,nmArq.length()).equals("csv")){
                jComboBox1.addItem(getSimbolo(arquivo.getName()));
            }
           
        }
 
    }
    public void setDiretorio(String diretorio){
        this.diretorioG = diretorio;
        incluirAtivos();
    }
    
    private String getSimbolo(String nome){
        String [] tokens = nome.split("\\.");
        return tokens[0];
    }
    
